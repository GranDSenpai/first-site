let currentSortOption = "difficulty";
let currentSortOrder = "asc";

function sortTable(option) {
  if (option === currentSortOption) {
    currentSortOrder = currentSortOrder === "asc" ? "desc" : "asc";
  } else {
    currentSortOption = option;
    currentSortOrder = "asc";
  }

  maps.sort((a, b) => {
    let aValue = a[currentSortOption];
    let bValue = b[currentSortOption];

    if (currentSortOption === "date") {
      aValue = new Date(aValue.split("-").reverse().join("-"));
      bValue = new Date(bValue.split("-").reverse().join("-"));
    }

    if (currentSortOption === "type") {
      aValue = aValue.toLowerCase();
      bValue = bValue.toLowerCase();
    }

    if (aValue < bValue) return currentSortOrder === "asc" ? -1 : 1;
    if (aValue > bValue) return currentSortOrder === "asc" ? 1 : -1;
    return 0;
  });

  populateTable();
}

const artistHeader = document.querySelector("#mapTable th:nth-child(2)");
const bpmHeader = document.querySelector("#mapTable th:nth-child(6)");
const dateHeader = document.querySelector("#mapTable th:nth-child(4)");
const difficultyHeader = document.querySelector("#mapTable th:nth-child(5)");
const typeHeader = document.querySelector("#mapTable th:nth-child(3)");

artistHeader.addEventListener("click", () => sortTable("artist"));
bpmHeader.addEventListener("click", () => sortTable("bpm"));
dateHeader.addEventListener("click", () => sortTable("date"));
difficultyHeader.addEventListener("click", () => sortTable("difficulty"));
typeHeader.addEventListener("click", () => sortTable("type"));